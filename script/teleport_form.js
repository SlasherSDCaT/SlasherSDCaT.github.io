var post = document.getElementById('postloader');
var teleport = false;

var spans = document.querySelectorAll('.link0');
for (var i=0; i<spans.length; i++) {
    spans[i].addEventListener('click', function() {
        teleport = confirm("При переходе внесенные изменения не сохранятся!");
        if (teleport) {
            post.style.visibility = 'visible';
            post.style.opacity = 1;
            setTimeout(function() {
                window.location.href = 'planet0.html';
            }, 600);
        }
    });
}

